void neverlose::fix_imports()
{
	auto logger = ENTER_LOGGER(logman);
	for (auto& import : g_imports)
	{
		HMODULE hmod = GetModuleHandle(import.module_name);
		if (!hmod)
			hmod = LoadLibrary(import.module_name);

		FARPROC proc = GetProcAddress(hmod, import.procname);

		InterlockedExchange((PLONG)import.address, (LONG)proc);

		logger << (proc ? "Fixed import " : "Failed to fix import ") << proc << " from " << import.module_name << " at " << (void*)import.address << '\n';
	};
};