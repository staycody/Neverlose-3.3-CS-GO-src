void neverlose::fix_cvars()
{
	auto logger = ENTER_LOGGER(logman);

	HMODULE hVstdlib = GetModuleHandle(L"vstdlib.dll");
	CreateInterfaceFn CreateInterface = reinterpret_cast<CreateInterfaceFn>(GetProcAddress(hVstdlib, "CreateInterface"));
	if (CreateInterface)
	{
		ICvar* cvar_interface = (ICvar*)CreateInterface("VEngineCvar007", nullptr);

		for (auto& [address, name] : g_convars)
		{
			PVOID pconvar = cvar_interface->FindVar(name);
			if (pconvar)
			{
				*(PPVOID)address = pconvar;
				logger << "Fixed ConVar at " << (PVOID)address << " for " << name << ' ' << pconvar << '\n';
			}
			else
				logger << "Failed to find ConVar " << name << '\n';
		};
	}
	else
		logger << "Failed to find CreateInterface in vstdlib\n";
};