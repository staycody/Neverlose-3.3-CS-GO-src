void neverlose::fix_interfaces()
{
	auto logger = ENTER_LOGGER(logman);

	for (auto& [address, module_name, interface_name] : g_interfaces)
	{
		HMODULE hMod = GetModuleHandle(module_name);
		if (!hMod)
			hMod = LoadLibrary(module_name);

		CreateInterfaceFn CreateInterface = reinterpret_cast<CreateInterfaceFn>(GetProcAddress(hMod, "CreateInterface"));
		if (CreateInterface)
		{
			PVOID pInterface = CreateInterface(interface_name, nullptr);
			if (pInterface)
			{
				*(PPVOID)address = pInterface;
				logger << "Fixed interface at " << (PVOID)address << " for " << interface_name << " from " << module_name << '\n';
			}
			else
				logger << "Failed to create interface " << interface_name << " from " << module_name << '\n';
		}
		else
			logger << "Failed to find CreateInterface for " << interface_name << " from " << module_name << '\n';
	};
};