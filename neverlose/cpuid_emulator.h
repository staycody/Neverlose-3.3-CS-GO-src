constexpr BYTE DEF_CPUID_EMU_TRAMPOLINE[] =
{
	// call rel32
	0xE8, 0x00, 0x00, 0x00, 0x00,
	// jmp [var]
	0xFF, 0x25, 0x00, 0x00, 0x00, 0x00,
	// pad
	0x00,
	// var
	0x00, 0x00, 0x00, 0x00
};

#pragma pack(push, 1)
struct cpuid_emu_emplacement
{
	union
	{
		BYTE data[16];
		struct
		{
			BYTE Call;
			DWORD Rel32;
			BYTE JmpOpcode[2];
			DWORD JmpVarAddress;
			BYTE Pad;
			DWORD JumpBackAddr;
		};
	};

	cpuid_emu_emplacement(PVOID absolute_call_address)
	{
		memcpy(data, DEF_CPUID_EMU_TRAMPOLINE, 16);

		JmpVarAddress = (DWORD)&JumpBackAddr;

		Rel32 = (DWORD)((INT32)absolute_call_address - (INT32)&Call - 5);
	};
};
#pragma pack(pop)
